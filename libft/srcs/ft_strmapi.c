#include "../includes/libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	int		s_length;
	char	*str;

	if (s == NULL)
		return (NULL);
	i = 0;
	s_length = ft_strlen(s);
	str = malloc(sizeof(*str) * s_length + 1);
	if (str == NULL)
		return (NULL);
	while (s[i] != '\0')
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
