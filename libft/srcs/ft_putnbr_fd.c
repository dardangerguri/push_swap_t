#include "../includes/libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	sign;

	if (n == -2147483648)
	{
		ft_putchar_fd('-', fd);
		ft_putchar_fd('2', fd);
		ft_putnbr_fd(147483648, fd);
	}
	else
	{
		if (n < 0)
		{
			sign = -1;
			ft_putchar_fd('-', fd);
			n = n * sign;
		}
		if (n < 10)
			ft_putchar_fd(n + 48, fd);
		if (n > 9)
		{
			ft_putnbr_fd(n / 10, fd);
			ft_putnbr_fd(n % 10, fd);
		}
	}
}
